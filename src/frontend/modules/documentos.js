/**
 * Clase que gestiona el estado completo de la subida múltiple de archivos.
 * Controla archivos pendientes, en progreso, completados y fallidos,
 * así como configuraciones comunes para todos los archivos.
 */
class MultipleUploadState {
    constructor() {
        this.files = [];
        this.currentUploads = [];
        this.completedUploads = [];
        this.failedUploads = [];
        this.isUploading = false;
        this.totalSize = 0;
        this.commonCategory = '';
        this.commonPersonId = '';
        this.expirationDays = null;
        this.uploadStrategy = 'sequential';
        this.autoGenerateDescriptions = true;
        this.notifyPerson = false;
    }

    /**
     * Reinicia completamente el estado de subida múltiple.
     * Útil para comenzar una nueva sesión de subida.
     */
    reset() {
        this.files = [];
        this.currentUploads = [];
        this.completedUploads = [];
        this.failedUploads = [];
        this.isUploading = false;
        this.totalSize = 0;
        this.commonCategory = '';
        this.commonPersonId = '';
        this.expirationDays = null;
    }

    /**
     * Agrega nuevos archivos al estado, validando cada uno individualmente.
     * Evita duplicados y aplica validaciones de tipo y tamaño.
     * @param {File[]} newFiles - Array de archivos a agregar
     */
    addFiles(newFiles) {
        console.group('📦 Agregando archivos múltiples');
        
        for (const file of newFiles) {
            // Verificar si ya existe
            const existingFile = this.files.find(f => 
                f.name === file.name && f.size === file.size
            );
            
            if (!existingFile) {
                // Validar archivo individual
                if (this.validateSingleFile(file)) {
                    const fileObj = {
                        file: file,
                        id: this.generateFileId(file),
                        status: 'pending',
                        progress: 0,
                        error: null,
                        description: file.name.replace(/\.[^/.]+$/, ""), // Nombre sin extensión
                        customCategory: '',
                        customPersonId: '',
                        customExpirationDate: null
                    };
                    
                    this.files.push(fileObj);
                    this.totalSize += file.size;
                    
                    console.log(`✅ Archivo agregado: ${file.name} (${formatFileSize(file.size)})`);
                }
            } else {
                console.log(`⚠️ Archivo duplicado ignorado: ${file.name}`);
            }
        }
        
        console.log(`📊 Total archivos: ${this.files.length}, Tamaño total: ${formatFileSize(this.totalSize)}`);
        console.groupEnd();
    }

    /**
     * Elimina un archivo del estado por su ID.
     * @param {string} fileId - ID único del archivo a eliminar
     * @returns {boolean} - True si se eliminó correctamente
     */
    removeFile(fileId) {
        const index = this.files.findIndex(f => f.id === fileId);
        if (index !== -1) {
            const removedFile = this.files[index];
            this.totalSize -= removedFile.file.size;
            this.files.splice(index, 1);
            console.log(`🗑️ Archivo removido: ${removedFile.file.name}`);
            return true;
        }
        return false;
    }

    /**
     * Valida un archivo individual según configuraciones del sistema.
     * Verifica tipo de archivo, tamaño individual y tamaño total acumulado.
     * @param {File} file - Archivo a validar
     * @returns {boolean} - True si el archivo es válido
     */
    validateSingleFile(file) {
        try {
            console.log(`🔍 Validando archivo: ${file.name}`);
            
            // Validar tipo de archivo
            const fileExtension = file.name.split('.').pop().toLowerCase();
            if (!CONFIG.ALLOWED_FILE_TYPES.includes(fileExtension)) {
                console.error(`❌ Tipo no permitido: ${fileExtension}`);
                showAlert(
                    `"${file.name}" - Tipo no permitido. Formatos aceptados: ${CONFIG.ALLOWED_FILE_TYPES.join(', ').toUpperCase()}`,
                    'error'
                );
                return false;
            }

            // Validar tamaño individual
            if (file.size > CONFIG.MAX_FILE_SIZE) {
                console.error(`❌ Tamaño excedido: ${formatFileSize(file.size)} > ${formatFileSize(CONFIG.MAX_FILE_SIZE)}`);
                showAlert(
                    `"${file.name}" - Excede el tamaño máximo por archivo (${formatFileSize(CONFIG.MAX_FILE_SIZE)})`,
                    'error'
                );
                return false;
            }

            // Validar tamaño total (si ya hay archivos)
            if (this.totalSize + file.size > CONFIG.MAX_TOTAL_UPLOAD_SIZE) {
                console.error(`❌ Tamaño total excedido`);
                showAlert(
                    `"${file.name}" - Excede el tamaño total permitido para múltiples archivos`,
                    'error'
                );
                return false;
            }

            console.log(`✅ Archivo válido: ${file.name}`);
            return true;

        } catch (error) {
            console.error(`❌ Error validando archivo ${file.name}:`, error);
            showAlert(`Error validando "${file.name}": ${error.message}`, 'error');
            return false;
        }
    }

    /**
     * Valida todos los archivos en el estado globalmente.
     * Verifica límites, categorías y otros requisitos del sistema.
     * @returns {boolean} - True si todos los archivos son válidos
     */
    validateAllFiles() {
        console.group('🔍 Validando todos los archivos');
        
        const errors = [];
        
        // Verificar límite de archivos
        if (this.files.length > CONFIG.MAX_MULTIPLE_FILES) {
            errors.push(`Máximo ${CONFIG.MAX_MULTIPLE_FILES} archivos permitidos`);
        }

        // Verificar tamaño total
        if (this.totalSize > CONFIG.MAX_TOTAL_UPLOAD_SIZE) {
            errors.push(`Tamaño total excedido (${formatFileSize(this.totalSize)} > ${formatFileSize(CONFIG.MAX_TOTAL_UPLOAD_SIZE)})`);
        }

        // Verificar que haya archivos
        if (this.files.length === 0) {
            errors.push('No hay archivos seleccionados');
        }

        // Verificar categoría si se requiere
        if (this.commonCategory === '' && this.files.some(f => f.customCategory === '')) {
            errors.push('Debe seleccionar una categoría para todos los archivos o individualmente');
        }

        if (errors.length > 0) {
            console.error('❌ Errores de validación:', errors);
            errors.forEach(error => showAlert(error, 'error'));
            console.groupEnd();
            return false;
        }

        console.log('✅ Todos los archivos son válidos');
        console.groupEnd();
        return true;
    }

    /**
     * Genera un ID único para un archivo basado en nombre, tamaño y timestamp.
     * @param {File} file - Archivo para generar ID
     * @returns {string} - ID único del archivo
     */
    generateFileId(file) {
        return `${file.name}_${file.size}_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
    }

    /**
     * Obtiene estadísticas actuales del estado de subida.
     * @returns {object} - Objeto con conteos de archivos por estado y tamaños
     */
    getStats() {
        return {
            total: this.files.length,
            pending: this.files.filter(f => f.status === 'pending').length,
            uploading: this.files.filter(f => f.status === 'uploading').length,
            completed: this.files.filter(f => f.status === 'completed').length,
            failed: this.files.filter(f => f.status === 'failed').length,
            totalSize: this.totalSize,
            uploadedSize: this.files
                .filter(f => f.status === 'completed')
                .reduce((sum, f) => sum + f.file.size, 0)
        };
    }

    /**
     * Muestra en consola el estado actual de la subida múltiple.
     * Útil para debugging y seguimiento de progreso.
     */
    logState() {
        console.group('📊 Estado de Subida Múltiple');
        const stats = this.getStats();
        console.table({
            'Total Archivos': stats.total,
            'Pendientes': stats.pending,
            'Subiendo': stats.uploading,
            'Completados': stats.completed,
            'Fallidos': stats.failed,
            'Tamaño Total': formatFileSize(stats.totalSize),
            'Subido': formatFileSize(stats.uploadedSize)
        });
        console.groupEnd();
    }
}

